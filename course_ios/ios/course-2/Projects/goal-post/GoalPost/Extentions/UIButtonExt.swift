import UIKit

extension UIButton{
    
    
    func selectedColor(){
        self.backgroundColor = #colorLiteral(red: 0.4274509804, green: 0.737254902, blue: 0.3882352941, alpha: 1)
    }
    
    func unselectedColor(){
        self.backgroundColor =   #colorLiteral(red: 0.4274509804, green: 0.737254902, blue: 0.3882352941, alpha: 0.5982359795)
    }
    
    
    
    
    
}
